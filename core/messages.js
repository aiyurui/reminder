// 获取随机消息
function getRandomMessage(reminderId) {
const messages = MESSAGE_LIBRARY[reminderId];
if (!messages || messages.length === 0) {
return '该休息了！';
}
const index = Math.floor(Math.random() * messages.length);
return messages[index];
}
